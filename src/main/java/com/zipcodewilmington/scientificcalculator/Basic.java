package com.zipcodewilmington.scientificcalculator;

public class Basic {

    public double add(double x , double y){
        return x+y;
    }
    public double multiply ( double x, double y){
         return x*y;
    }
    public double divide ( int x, int y){
        return x/y;
    }
    public double subtract (int x, int y){
        return x-y;
    }
}
